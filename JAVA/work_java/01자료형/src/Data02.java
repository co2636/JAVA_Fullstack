
// 예제 : 더 다양한 종류의 상수들 출력 
public class Data02 {

	public static void main(String[] args) {

		//(1) long 형 정수형태의 상수 : 숫자 끝에 L 혹은 l 접미사를 붙여 표현한 수
		System.out.println(1L);
		//(2) float 형 실수형태의 상수 : 숫자 끝에 F 혹은 f 접미사를 붙여 표현한 
		System.out.println(1.5f);
		//(3) 레퍼런스형 중에서 문자열 객체 출력!
		// 문자열 객체는 이중 따옴표 "문자열"로 묶어준 객체 
		System.out.println("abc");
		
		
		
		
	}

}
