
//예제 : 자바 문법에서 사용되는 상수의 종류 출력 해보기 

public class Data01 {

	// main 메소드 : 프로그램을 실행하는 시작점 
	public static void main(String[] args) {
		
		//(1) 정수 : 소수점이 없는 숫자
		System.out.println(1);
		
		//(2)실수 : 소수점이 있는 숫자
		System.out.println(1.5);
		
		//(3) 문자 : ''단일 따옴표로 묶은 하나의 데이터
		//		System.out.print("a \n");
		System.out.println('a');
		
		//(4) 논리 : true 참  or false 거짓의 데이터 
		System.out.println(true);

		//(5)  
		System.out.println();
	}

}
